# Definindo a variável texto
texto = 'Olá, laço for.'

# Laço for que itera sobre cada caractere da variável texto
for item in texto:
    print(f'Caractere: {item}')

# Nova instrução for que itera sobre um intervalo numérico entre 1 e 10
for numero in range(1, 11):
    print('Número do intervalo: ' + str(numero))
